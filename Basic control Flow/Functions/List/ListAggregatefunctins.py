runs=[10,20,30,40,50]
print(sum(runs)) #sum of all the elements in the list
print(max(runs)) #maximum value in the list
print(min(runs)) #minimum value in the list
print(len(runs)) #number of elements in the list
print(sorted(runs)) #sort the list in ascending order   
print(sorted(runs,reverse=True)) #sort the list in descending order
print(sum(runs)/len(runs)) #average of the elements in the list   